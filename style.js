///Age
var answers = prompt("How old are you?");

if (answers > 23) {
    confirm("Because you're " + answers + ", you are older than me.")
} else if (answers < 23){
    confirm("Because you're " + answers + ", you are younger than me.")
} else if (answers = 23){
    confirm("Because you're " + answers + ", we are the same age.")
}


